
package modelo;

public class Convertidor {
    int decimal;
    String resulatado;

    public Convertidor(int decimal, String resulatado) {
        this.decimal = decimal;
        this.resulatado = resulatado;
    }

    public int getDecimal() {
        return decimal;
    }

    public void setDecimal(int decimal) {
        this.decimal = decimal;
    }

    public String getResulatado() {
        return resulatado;
    }

    public void setResulatado(String resulatado) {
        this.resulatado = resulatado;
    }
    
   
}
